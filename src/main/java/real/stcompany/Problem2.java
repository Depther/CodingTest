package real.stcompany;

public class Problem2 {

	// S안에 인접한 두 문자를 다이어그램이라고 한다.
	// 다이어그램 사이의 거리는 각 다이어그램의 첫 문자 사이의 거리
	// S안에서 가장 먼 두 일치 다이어그램 사이의 거리를 반환하는 함수를 만들어라

	public int solution(String S) {
		int answer = -1;

		// 길이가 2보다 작은 경우 두 개 이상의 다이어그램이 생기지 않으므로 제외
		if (S.length() <= 2) {
			return answer;
		}


		return answer;
	}

}
